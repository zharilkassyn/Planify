export function buildPlanifySystemPrompt(options: BuildPlanifySystemPromptOptions = {}) {
  const mode = options.mode ?? 'assistant';
  const modeInstruction = getModeInstruction(mode);
  const state = options.appState ? `\n\nТекущий контекст приложения:\n${options.appState}` : '';
  return `${PLANIFY_AI_CONTEXT}\n\nРежим сейчас: ${mode}.\n${modeInstruction}${state}`;
}

function getModeInstruction(mode: PlanifyAiMode) {
  switch (mode) {
    case 'flashcards':
      return [
        'Создавай полноценные флеш-карты.',
        'Если указано количество, верни ровно столько карточек.',
        'Ответ каждой карточки может включать краткое объяснение, перевод или пример, если это полезно для темы.',
      ].join('\n');
    case 'presentation':
      return [
        'Создавай структуру настоящей презентации, а не набор текстовых страниц.',
        'Каждый слайд должен иметь layout, короткий текст, визуальную роль и visualPrompt.',
        'Шаблон презентации должен влиять на дизайн и визуальные подсказки.',
      ].join('\n');
    case 'schedule':
      return [
        'Создавай расписание, которое можно сразу сохранить в планировщик.',
        'Если пользователь просит план на день/неделю, создай все нужные события, не один пример.',
      ].join('\n');
    case 'notes':
      return [
        'Создавай готовый структурированный конспект.',
        'Используй заголовки, списки, определения, важные мысли и выводы.',
      ].join('\n');
    case 'assistant':
    default:
      return [
        'Определи, нужна ли пользователю функция приложения или текстовый ответ.',
        'Если задача относится к функции Planify, помоги приложению выполнить её полностью.',
      ].join('\n');
  }
}
