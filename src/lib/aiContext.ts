export type PlanifyAiMode =
  | 'assistant'
  | 'flashcards'
  | 'presentation'
  | 'schedule'
  | 'notes';

interface BuildPlanifySystemPromptOptions {
  mode?: PlanifyAiMode;
  appState?: string;
}

